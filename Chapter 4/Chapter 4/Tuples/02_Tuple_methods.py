# count(ele) --> it returns how many elements are present

tuples = (1, 2, 4, 6, 2, 99, 1)
print(tuples.count(4)) 
print(tuples)

# index() --> it returns the index value of its 1st occurance

tuples = (1, 2, 4, 6, 2, 99, 1)
print(tuples.index(99)) 
print(tuples)

# index(start, end moves)

tuples = (1, 2, 4, 6, 2, 99, 1)
print(tuples.index(1)) 
print(tuples)